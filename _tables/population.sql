-- Create population table
DROP TABLE IF EXISTS datasets.population;
CREATE TABLE datasets.population (
    -- ISO 3166-1 alpha-2 code
    region_code VARCHAR(2) NOT NULL,
    
    -- population figures for census years
    pop_2021 INT,
    pop_2010 INT,
    pop_2000 INT,
    pop_1984 INT,
    pop_1970 INT,
    pop_1960 INT
);


